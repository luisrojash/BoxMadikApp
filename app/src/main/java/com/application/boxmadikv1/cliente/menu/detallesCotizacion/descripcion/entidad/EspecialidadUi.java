package com.application.boxmadikv1.cliente.menu.detallesCotizacion.descripcion.entidad;

public class EspecialidadUi {
    private String codigo;
    private String descripcion;
    private String PE_estado;

    public EspecialidadUi() {
    }

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public String getPE_estado() {
        return PE_estado;
    }

    public void setPE_estado(String PE_estado) {
        this.PE_estado = PE_estado;
    }
}
